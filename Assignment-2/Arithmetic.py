# add two numbers
def Add(no1,no2):
	return no1 + no2

# Subtract two numbers
def Sub(no1,no2):
	return no1 - no2

# Division 
def Div(no1,no2):
	return no1 // no2

#Multipliction
def Multi(no1,no2):
	return no1 * no2